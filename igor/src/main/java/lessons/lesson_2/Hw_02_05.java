public class Hw_02_05 {
    public static void main(String[] args) {
        byte b1 = 6 + 5;
        short sh1 = 8 - 5;
        int i = 9 / 3;
        long l = 6 * 3;

        System.out.println("b1 = " + b1);
        System.out.println("sh1 = " + sh1);
        System.out.println("i = " + i);
        System.out.println("l = " + l);
        System.out.println();
    }
}
