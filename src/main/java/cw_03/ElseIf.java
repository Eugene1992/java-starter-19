package cw_03;

public class ElseIf {
    public static void main(String[] args) {
        int k = 1;

        if (k > 0) {
            System.out.println("k > 0");
        } else if (k < 0){
            System.out.println("k < 0");
        } else {
            System.out.println("k = 0");
        }


    }
}
